class Student:
    def __init__(self, id, firstName, lastName,
                 creationTime, lastUpdated, existingMagicSkills,
                 desiredMagicSkills, interestedInCourse
                 ):
        self.id = id
        self.firstName = firstName
        self.lastName = lastName
        self.creationTime = creationTime
        self.lastUpdated = lastUpdated
        self.existingMagicSkills = existingMagicSkills
        self.desiredMagicSkills = desiredMagicSkills
        self.interestedInCourse = interestedInCourse
